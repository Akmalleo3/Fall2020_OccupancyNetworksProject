bool sortVertices(Vertex v1, Vertex v2){ return v1.globalIdx < v2.globalIdx;}


struct VertexComparator{
    bool operator()(const Vertex &v1, const Vertex &v2){
      float dist = std::sqrt((v1.x-v2.x)*(v1.x-v2.x) + (v1.y-v2.y)*(v1.y-v2.y) +
                             (v1.z-v2.z)*(v1.z-v2.z));
      
      if (dist < 0.0000001)
        return false;

      if (fabs(v1.x - v2.x) > 0.001)
        return v1.x < v2.x;
      if (fabs(v1.y - v2.y) > 0.001)
        return v1.y < v2.y;
      if (fabs(v1.z - v2.z) > 0.001)
        return v1.z < v2.z;
      return false;

      }
};




struct FaceComparator{
    bool operator()(const Face &f1, const Face &f2){
      if (f1.v1 < f2.v1)
        return true;
      if (f1.v2 < f2.v2)
        return true;
      if (f1.v3 < f2.v3)
        return true;
      return false;
      }
};

void writeMeshFile(std::set<Vertex,VertexComparator> &vsset, std::set<Face,FaceComparator> &faces, std::string meshFileName){

  std::vector<Vertex> vertices(vsset.begin(), vsset.end());
  std::sort(vertices.begin(), vertices.end(), sortVertices);
  std::ofstream out(meshFileName);

  out << "OFF" << std::endl;
  out << vertices.size() << "  " << faces.size() << " 0 " << std::endl;
  for(auto v:vertices){
    out << v.x << " " << v.y << " " << v.z << std::endl;
  }
  for(auto f:faces){
    out << "3 " << f.v1 << " " << f.v2 << " " << f.v3 << std::endl;
  }

  out.close();

}

void MarchingCubes::march(std::vector<Cube> & cubes, std::string meshFileName){

  std::set<Vertex, VertexComparator> vertices;
  std::set<Face, FaceComparator> faces;
  int vCount = 0;
  for(auto cube: cubes){
    //Calculate the cube index indicating which edges are split
    int cubeindex = 0;
    int vidx = 1;
    for(auto v:cube.vertices){
      if (v.occupies){
          cubeindex |= vidx;
        }
      vidx *= 2;
    }


    if (edgeTable[cubeindex] == 0)
      continue; //This cube lies entirely inside or outside of the mesh

    //Read out the edges involved in the intersection
    int edges = edgeTable[cubeindex];
    int mask = 1;

    std::unordered_map<int, int> localToGlobalIdx;
    std::unordered_map<int, Vertex> edgeIdxToVertex;
    for(int edgeIdx = 0; edgeIdx < 12; edgeIdx++){
      if (edges & mask){
        //estimate location of mesh intersection
        Vertex v(estimateIntersectionPoint(cube, edgeIdx));

        //store so for face ordering down below
        edgeIdxToVertex.insert(std::pair<int,Vertex>(edgeIdx, v));

        //track vertices globally with an index
        auto it = vertices.find(v);
        if (it == vertices.end()){
          v.globalIdx = vCount;
          vertices.insert(v);
          localToGlobalIdx.insert(std::pair<int, int>(edgeIdx, vCount));
          if(std::isnan(v.x) || std::isnan(v.y) || std::isnan(v.z)){
            std::cout << "Vertex with nan Coordinate" << std::endl;
            assert(-1);
            }
           vCount++;

        }
        else{
          localToGlobalIdx.insert(std::pair<int, int>(edgeIdx, it->globalIdx));
          //std::cout << "Edge idx: " << edgeIdx << " maps to global vertx: " << it->x <<
          //  "  " << it->y << "it->z" << std::endl;
          //std::cout << "From v: " << v.x << " " << v.y << " " << v.z << std::endl;
        }

      }
      mask = mask*2;
    }

    //Read out the triangles formed
    for (int i=0;triTable[cubeindex][i]!=-1;i+=3) {
      int v1_idx = triTable[cubeindex][i];
      int v2_idx= triTable[cubeindex][i+1];
      int v3_idx = triTable[cubeindex][i+2];

      const Vertex v1 = edgeIdxToVertex.at(v1_idx);
      const Vertex v2 = edgeIdxToVertex.at(v2_idx);
      const Vertex v3 = edgeIdxToVertex.at(v3_idx);
      Eigen::Vector3f vector1(v2.x-v1.x, v2.y-v1.y, v2.z-v1.z);
      Eigen::Vector3f vector2(v3.x-v1.x, v3.y-v1.y, v3.z-v1.z);
      Eigen::Vector3f normal = vector1.cross(vector2);
      Eigen::Vector3f light(0.01,0.01,0.01);

      v1_idx = localToGlobalIdx[v1_idx];
      v2_idx= localToGlobalIdx[v2_idx];
      v3_idx = localToGlobalIdx[v3_idx];

      Face f;
      if (normal.dot(light) < 0){
      f.v1 = v1_idx;
      f.v2 = v3_idx;
      f.v3 = v2_idx;
      }
      else{
        f.v1 = v1_idx;
        f.v2 = v2_idx;
        f.v3 = v3_idx;
      }
      if ((f.v1 == f.v2) || (f.v1 == f.v3) || (f.v2 == f.v3)){
        std::cout << "Face with repeat vertices" << std::endl;
        //std::cout << "Face: " << f.v1 << "  " << f.v2 << "  " << f.v3 << std::endl;
        //std::cout << "vertices" << v1 << "  " << v2 << "  " << v3 << std::endl;

        assert(-1);
          }
      faces.insert(f);
    }
  }
  writeMeshFile(vertices, faces, meshFileName);
}

std::vector<Cube> MarchingCubes::assembleCubes(std::string pointsFile, std::string occupanciesFile){

  std::vector<Cube> cubes;
  readCoordinatesFromFile(cubes, pointsFile);

  float threshold = 0.1;
  std::ifstream occFile;
  occFile.open(occupanciesFile, std::ifstream::in);

  int cubeidx =-1;
  int vtxidx = -1;
  bool occupied;
  float pred;
  while(occFile.good()){
    occFile >> pred;
    //make like an implicit function and be 0 if (probably) on the mesh
    if(pred >= threshold)
      occupied = true;
    else {
      occupied = false;
    }
    if(vtxidx % 8 == 0){
      cubeidx += 1;
      vtxidx = 0;
    }
    if(cubeidx < cubes.size())
      cubes[cubeidx].vertices[vtxidx].occupies = occupied;
    vtxidx += 1;
  }
  occFile.close();
  return cubes;
}

